    //Metodo listar na tabela
    public void listar()
    {
        BandeiraDAO dao = new BandeiraDAO();
        List<Bandeira> lista = dao.listarBandeira();
        
        DefaultTableModel dados = (DefaultTableModel) tabBandeira.getModel();
        
        dados.setNumRows(0);
        
        for(Bandeira b: lista)
        {
            dados.addRow(new Object[]{
            
                b.getId_bandeira(),
                b.getNome()
                
            });
        }
        
    }
    
