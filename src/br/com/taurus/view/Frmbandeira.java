    private void tabBandeiraMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabBandeiraMouseClicked
        
        //Ao selecionar uma linha da grid, os campos são preenchidos.
        txtId.setText(tabBandeira.getValueAt(tabBandeira.getSelectedRow(), 0).toString());
        txtNome.setText(tabBandeira.getValueAt(tabBandeira.getSelectedRow(), 1).toString());
        
    }//GEN-LAST:event_tabBandeiraMouseClicked

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        
        String nome = "%" + txtNomePesquisa.getText() + "%";
        
        BandeiraDAO dao = new BandeiraDAO();
        List<Bandeira> lista = dao.pesquisar(nome);
        
        DefaultTableModel dados = (DefaultTableModel) tabBandeira.getModel();
        
        dados.setNumRows(0);
        
        for(Bandeira b: lista)
        {
            dados.addRow(new Object[]{
            
                b.getId_bandeira(),
                b.getNome()
                
            });
        }
        
    }//GEN-LAST:event_btnPesquisarActionPerformed

