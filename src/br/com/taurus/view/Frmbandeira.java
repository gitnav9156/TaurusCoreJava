    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        
        Bandeira bandeiraObj = new Bandeira();
        
        bandeiraObj.setId_bandeira(Integer.parseInt(txtId.getText()));
        bandeiraObj.setNome(txtNome.getText());

        BandeiraDAO dao = new BandeiraDAO();
        
        dao.editar(bandeiraObj);
        
        new Utilitarios().LimpaTela(jPanelBandeira);
        
    }//GEN-LAST:event_btnEditarActionPerformed

